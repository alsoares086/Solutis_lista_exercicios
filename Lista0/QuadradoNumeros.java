public class QuadradoNumeros {
    public static void main(String[] args) {
        // Loop para calcular o quadrado dos números de 0 a 10
        for (int i = 0; i <= 10; i++) {
            int quadrado = i * i;
            System.out.println("O quadrado de " + i + " é: " + quadrado);
        }
    }
}
